package com.jornadajava;

public class OperadorIgualdade {

    public static void main(String[] args) {
        int valorA = 10;
        int valorB = 11;

        if (valorA == valorB) {
            System.out.println("Os valores são iguais");
            //caso os valores das variáveis sejam iguais, a mensagem acima será exibida
        } else {
            System.out.println("Os valores são diferentes");
            //caso os valores das variáveis sejam diferentes, a mensagem acima será exibida
        }
    }
}
