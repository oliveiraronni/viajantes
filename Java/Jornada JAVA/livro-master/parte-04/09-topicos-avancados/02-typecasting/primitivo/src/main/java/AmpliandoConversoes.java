public class AmpliandoConversoes {

    static int variavelInt = 127;
    static long variavelLong = variavelInt;

    static float variavelFloat = variavelLong;
    static double variavelDouble = variavelLong;

    public static void main(String[] args) {
        System.out.println(variavelInt);
        System.out.println(variavelLong);
        System.out.println(variavelFloat);
        System.out.println(variavelDouble);
    }

}
